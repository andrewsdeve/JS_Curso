// Algunas operaciones con numeros

const numero1 = 20;
const numero2 = 50;
let resultado;

// Sumar

resultado = numero1 + numero2;
console.log(resultado);


//Resta

resultado = numero1 - numero2;
console.log(resultado);


//Multiplicacion

resultado = numero1 * numero2;
console.log(resultado);



//Divicion

resultado = numero1 / numero2;
console.log(resultado);




//Modulo

resultado = numero1 % numero2;
console.log(resultado);

